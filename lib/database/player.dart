class Player {
  final int id;
  final String name;
  final int status;
  final int position;
  final int team;
  final double winRate;
  final int wins;
  final int losses;
  final int attendance;

  Player({
    required this.id,
    required this.name,
    required this.status,
    required this.position,
    required this.team,
    required this.winRate,
    required this.wins,
    required this.losses,
    required this.attendance,
  });
}